package fiuba.algo3.algocraft.unidades.parametros;

public class Danio {
    private int tierra;
    private int aire;
    private int rango;

    public Danio(int tierra, int aire, int rango) {
        this.tierra = tierra;
        this.aire = aire;
        this.rango = rango;
    }

    public int getTierra() {
        return tierra;
    }

    public int getAire() {
        return aire;
    }

    public int getRango() {
        return rango;
    }
}
